#include "fillit.h"

int		setminsize(t_list *lst)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (lst != 0)
	{
		lst = lst->next;
		i++;
	}
	while (i * 4 > j * j)
		j++;
	return (j);
}
